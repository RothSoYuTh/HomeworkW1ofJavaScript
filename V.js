//Switch case

let grade = 'A'

switch(grade) {
    case 'A':
        console.log("Exellent");
        break;
    case 'B':
        console.log("Good");
        break;
    case 'C':
        console.log("Average");
        break;
    default:
        console.log("Try harder!");
    
}

let fruit0 = "Banana";
let fruit1 = fruit0.toLowerCase();

switch(fruit1) {
    case "banana":
        console.log("Yellow");
        break;
    case "grape":
        console.log("Purple");
        break;
    case "Apple":
        console.log("Black");
        break;
}