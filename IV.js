//Conditional Ternary Operators

let A = 3;

A % 2 == 0 ? console.log("It is even.") : console.log("It is odd.");

let age = 19;

age >= 18 ? console.log("Adult.") : console.log("Minor.");

let isStudent = true;

isStudent ? console.log("Student.") : console.log("Not a student.");

let score = 100;

score >= 50 ? console.log("Pass.") : console.log("Fail.");

let temp = 43;

temp > 35 ? console.log("Too hot.") : console.log("Normal.");